  CREATE OR REPLACE FUNCTION "DBACD8ESB"."FORMATNUMBER" ( param in VARCHAR )
RETURN varCHAR
AS 
    result VARCHAR(200);
BEGIN 

	SELECT param INTO result FROM DUAL;

    IF ( param IS null ) THEN
    BEGIN
    	RETURN NULL;
    END;
    END IF;
    
    IF ( param LIKE '%.0' ) THEN
    BEGIN
    	SELECT substr(param, 0, length(param)-2) INTO result FROM DUAL;
    END;
    END IF;

    return (result); 
END;
